public class Medico {
    String idMedico;
    String nombre;

    public Medico(String idMedico, String nombre)
    {
        this.idMedico = idMedico;
        this.nombre = nombre;
    }

    public void setIdMedico(String idMedico)
    {
        this.idMedico = idMedico;
    }

    public void setNombre(String nombre)
    {
        this.nombre = nombre;
    }

    public String getIdMedico()
    {
        return idMedico;
    }

    public String nombre()
    {
        return nombre;
    }
    
}
